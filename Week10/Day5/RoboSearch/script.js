var cardsList = document.getElementById("cardsList")
for (item of robots){
    newCard = document.createElement('li')
    newCard.classList.add("profiles")

    newProfile = document.createElement('img')
    newProfile.src = item.image
    
    newCard.appendChild(newProfile)

    newName = document.createElement('h2')
    newName.textContent = item.name
    newCard.appendChild(newName)

    newEmail = document.createElement('p')
    newEmail.textContent = item.email
    newCard.appendChild(newEmail)

    cardsList.appendChild(newCard)
}

function searcher(){
    var search, filter, cards, name, i, txtValue
    search = document.getElementById('search')
    filter = search.value.toUpperCase()
    cards = document.getElementsByClassName('profiles')

    for (i=0; i < cards.length; i++) {
        name = cards[i].getElementsByTagName("h2")[0]
        txtValue = name.textContent.toUpperCase()
        if (txtValue.indexOf(filter) > -1) {
            cards[i].style.display = "";
          } else {
            cards[i].style.display = "none";
          }
    }
}